    @Override
    public void setRegistry(SDbRegistry registry) throws Exception {
        moRegistry = (SDbSalesAgent) registry;

        mnFormResult = SLibConsts.UNDEFINED;
        mbFirstActivation = true;

        removeAllListeners();
        reloadCatalogues();

        if (moRegistry.isRegistryNew()) {
            jtfRegistryKey.setText("");
        }
        else {
            jtfRegistryKey.setText(SLibUtils.textKey(moRegistry.getPrimaryKey()));
        }

        jtfCode.setText(moRegistry.getCode());
        jtfCode.setCaretPosition(0);
        jtfName.setText(moRegistry.getName());
        jtfName.setCaretPosition(0);
        moIntDesSalesAgentId.setValue(moRegistry.getDesSalesAgentId());

        setFormEditable(true);
        
        enableEditDesSalesAgentId(false);
        
        if (moRegistry.isRegistryNew()) {
        }
        else {
        }
        
        addAllListeners();
    }

    @Override
    public SDbSalesAgent getRegistry() throws Exception {
        SDbSalesAgent registry = moRegistry.clone();

        if (registry.isRegistryNew()) {}

        registry.setDesSalesAgentId(moIntDesSalesAgentId.getValue());

        return registry;
    }

    @Override
    public SGuiValidation validateForm() {
        SGuiValidation validation = moFields.validateFields();
        
        return validation;
    }

    @Override
    public void actionPerformed(ActionEvent e) {
        if (e.getSource() instanceof JButton) {
            JButton button = (JButton) e.getSource();
            
            if (button == jbEditDesSalesAgentId) {
                actionEditDesSalesAgentId();
            }
        }
    }
