    private void enableEditDesSalesAgentId(boolean enable) {
        moIntDesSalesAgentId.setEditable(enable);
        jbEditDesSalesAgentId.setEnabled(!enable);
    }
    
    private void actionEditDesSalesAgentId() {
        enableEditDesSalesAgentId(true);
        moIntDesSalesAgentId.requestFocus();
    }
    
